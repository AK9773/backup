package simpli.learn;

public class DoWhileLoopAndWhileLoop {
	public static void main(String[] args) {
		int count = 1;
		do {
			System.out.println(count);
			count++;

		} while (count <= 10);

		System.out.println("**********************");
		int i = 1;
		while (i <= 10) {
			System.out.println("hello" + ": " + i);
			i++;
		}
	}

}
